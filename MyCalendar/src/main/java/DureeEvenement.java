public class DureeEvenement {

    private int minutes;

    public DureeEvenement(int minutes) {
        this.minutes = minutes;
    }

    public int getMinutes() {
        return minutes;
    }

    public void setMinutes(int minutes) {
        this.minutes = minutes;
    }

    public int getDuree() {
        return minutes;
    }
}
